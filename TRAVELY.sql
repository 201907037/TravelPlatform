CREATE TABLE DISTRICTS(
   LCODE_NO NUMBER CONSTRAINT DISTRICTS_PK PRIMARY KEY,
   LOCATION_NAME VARCHAR2(100) CONSTRAINT LNAME_NN NOT NULL,
   LOCATION_XX NUMBER,
   LOCATION_YY NUMBER
);

CREATE TABLE CITY(
    SIGUNGU_CODE_NO NUMBER CONSTRAINT CITY_PK PRIMARY KEY,
    SIGUNGU_CODE NUMBER CONSTRAINT CITY_CODE_NN NOT NULL,
    SIGUNGU_NAME VARCHAR2(80),
    CITY_XX NUMBER,
    CITY_YY NUMBER,
    REF_LCODE NUMBER CONSTRAINT FK_CITY REFERENCES DISTRICTS(LCODE_NO)
);

CREATE TABLE TOUR(
    TOUR_NO NUMBER CONSTRAINT TOUR_PK PRIMARY KEY,
    TOUR_NAME VARCHAR2(200),
    ADDRESS VARCHAR2(200),
    ADD_DATE DATE DEFAULT SYSDATE,
    CONTENT_ID VARCHAR2(100),
    THUMB_IMG VARCHAR2(200),
    MAP_X NUMBER,
    MAP_Y NUMBER,
    TOUR_TYPE VARCHAR2(50),
    STATUS CHAR(1) DEFAULT 'Y',
    REF_AREA NUMBER CONSTRAINT FK_AREA REFERENCES DISTRICTS(LCODE_NO),
    REF_CITY NUMBER 
);
CREATE TABLE TOUR_SPOT(
    SPOT_NO NUMBER CONSTRAINT TSPOT_PK PRIMARY KEY,
    SEASON VARCHAR2(100),
    USE_TIME VARCHAR2(100),
    TEL VARCHAR2(40),
    PET CHAR(1),
    REF_TNO NUMBER CONSTRAINT FK_SPOT REFERENCES TOUR(TOUR_NO)
);

CREATE TABLE LODGING(
    L_NO NUMBER CONSTRAINT LODGING_PK PRIMARY KEY,
    ROOM_TYPE CHAR(9),
    CHECK_IN VARCHAR2(30),
    CHECK_OUT VARCHAR2(30),
    COOK CHAR(1),
    TEL VARCHAR2(40),
    PARKING CHAR(1),
    REF_TNO NUMBER CONSTRAINT FK_LODGING REFERENCES TOUR(TOUR_NO)
);

CREATE TABLE LEPORTS(
    L_NO NUMBER CONSTRAINT LEPORTS_PK PRIMARY KEY,
    USE_TIME VARCHAR2(100),
    PARKING CHAR(1),
    PET CHAR(1),
    TEL VARCHAR2(40),
    REF_TNO NUMBER CONSTRAINT FK_LEPORTS REFERENCES TOUR(TOUR_NO)
);

CREATE TABLE RESTAURANT(
    RD_NO NUMBER CONSTRAINT RD_PK PRIMARY KEY,
    KID_ROOM CHAR(1),
    FIRST_MENU VARCHAR2(100),
    TREAT_MENU VARCHAR2(100),
    TEL VARCHAR2(40),
    OPEN_TIME VARCHAR2(100),
    REST_DATE VARCHAR2(100),
    REF_TNO NUMBER CONSTRAINT FK_REST REFERENCES TOUR(TOUR_NO)
);

CREATE TABLE TOUR_IMG(
    TIMG_NO NUMBER CONSTRAINT TIMG_PK PRIMARY KEY,
    CHANGE_NO VARCHAR2(200),
    IMG_URL VARCHAR2(200),
    STATUS CHAR(1) DEFAULT 'Y' CHECK(STATUS IN('Y','N')),
    REF_TNO NUMBER CONSTRAINT FK_TOUR_IMG REFERENCES TOUR(TOUR_NO)
);

CREATE TABLE TOUR_REVIEW(

    REVIEW_NO NUMBER PRIMARY KEY,
    REVIEW_TITLE VARCHAR2(100),
    REVIEW_CONTENT VARCHAR2(100),
    CREATE_DATE DATE DEFAULT SYSDATE,
    SCORE NUMBER,
    STATUS VARCHAR2(1) DEFAULT 'Y' NOT NULL,
    TOUR_NO NUMBER,
    USER_NO NUMBER,
    FOREIGN KEY(USER_NO) REFERENCES MEMBER(USER_NO),
    FOREIGN KEY(TOUR_NO) REFERENCES TOUR(TOUR_NO)
);

CREATE TABLE PLANNER(
    PLAN_NO NUMBER CONSTRAINT PLAN_PK PRIMARY KEY,
    PLAN_NAME VARCHAR2(100),
    PLAN_EXP VARCHAR2(300),
    START_DATE VARCHAR2(40),
    END_DATE VARCHAR2(40),
    AREA_CODE NUMBER,
    SIGUNGUCODENO NUMBER,
    CHANGE_NAME VARCHAR2(300),
    PL_STATUS CHAR(1) DEFAULT 'Y',
    CREATE_DATE DATE DEFAULT SYSDATE,
    COUNT NUMBER DEFAULT 0,
    STATUS CHAR(1) DEFAULT 'Y',
    REF_UNO NUMBER CONSTRAINT FK_PLANNER REFERENCES MEMBER(USER_NO)
);

CREATE TABLE PLAN_DETAIL(
    DETAIL_NO NUMBER CONSTRAINT PDETAIL_PK PRIMARY KEY,
    START_TIME VARCHAR2(100),
    END_TIME VARCHAR2(100),
    PLAN_DATE DATE,
    TIME_AMOUNT NUMBER,
    DAY_COUNT NUMBER,
    REF_TNO NUMBER CONSTRAINT FK_PDETAIL_TOUR REFERENCES TOUR(TOUR_NO),
    REF_PNO NUMBER CONSTRAINT FK_PDETAIL_PLAN REFERENCES PLANNER(PLAN_NO)
);

