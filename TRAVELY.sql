DECLARE
    TYPE comments_array IS VARRAY(10) OF VARCHAR2(300);
    comments comments_array := comments_array(
        '정말 멋진 여행지네요! 꼭 가보고 싶어요.',
        '사진이 너무 아름다워요! 잘 봤습니다.',
        '여행 후기 정말 공감돼요. 좋은 정보 감사합니다!',
        '여행 가고 싶어지네요! 좋은 글 감사합니다.',
        '풍경이 너무 멋져요! 가슴이 뻥 뚫리네요.',
        '여행지 선택에 많은 도움이 됐어요. 고마워요!',
        '정말 꿈같은 여행이었겠어요. 부러워요!',
        '멋진 사진들 덕분에 대리만족하고 갑니다!',
        '여행 정보가 유익했어요. 감사해요!',
        '정말 좋은 경험이었을 것 같아요. 잘 읽었어요!'
    );
BEGIN
    FOR b_no IN 1..32 LOOP
        DECLARE
            used_comments comments_array := comments_array();
        BEGIN
            FOR i IN 1..3 LOOP
                -- 중복되지 않는 댓글 내용 선택
                LOOP
                    EXIT WHEN used_comments.COUNT = 10; -- 모든 댓글을 사용한 경우 루프 종료
                    DECLARE
                        idx PLS_INTEGER := ROUND(DBMS_RANDOM.VALUE(1, 10));
                        new_comment VARCHAR2(300) := comments(idx);
                        random_hours NUMBER := ROUND(DBMS_RANDOM.VALUE(1, 24));
                    BEGIN
                        IF NOT used_comments.EXISTS(idx) THEN
                            used_comments.EXTEND;
                            used_comments(used_comments.COUNT) := new_comment;

                            INSERT INTO REPLY (REPLY_NO, REPLY_CONTENT, CREATE_DATE, MODIFY_DATE, STATUS, USER_NO, BOARD_NO)
                            VALUES (SEQ_REPLY_NO.NEXTVAL, 
                                    new_comment, 
                                    SYSDATE - (random_hours / 24), 
                                    NULL, 
                                    'Y', 
                                    ROUND(DBMS_RANDOM.VALUE(2, 19)), 
                                    b_no);
                            EXIT;
                        END IF;
                    END;
                END LOOP;
            END LOOP;
        END;
    END LOOP;
END;
/

COMMIT;

